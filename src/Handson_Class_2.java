public class Handson_Class_2 {
	public static void main(String[] args){
		int starting_number = 88;	// Sets the starting number for the loop to start at
		int ending_number = 103;	// Sets the ending number for the loop to end at 
		for(int i = starting_number; i <= ending_number; i++){	
			System.out.println(i);	// Prints the value of i to the console
		}// End of for loop
	}// End of main method
}// End of class
